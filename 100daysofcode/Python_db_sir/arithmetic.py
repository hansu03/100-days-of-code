result = 10 % 3
print(result)  # Output: 1

result = 2 ** 3
print(result)  # Output: 8

result = 2 + 3 * 4
print(result)  # Output: 14 (3 * 4 is performed first, then added to 2)

result = (2 + 3) * 4
print(result)  # Output: 20 (2 + 3 is performed first, then the result is multiplied by 4)


